Map::Map()
{
    dirt = TextureManager::LoadTexture("assets/textures/dt.png");
    grass = TextureManager::LoadTexture("assets/textures/d1.png");
    water = TextureManager::LoadTexture("assets/textures/water.png");

    LoadMap(lvl1);

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;

    dest.x = dest.y = 0;

}

void Map::LoadMap(int arr[24][40])
{
    for (int row = 0; row<24; row++)
    {
        for (int col = 0; col < 40; col++)
        {
            map[row][col] = arr[row][col];
        }
    }

}

void Map::DrawMap()
{
    int type = 0;

    for (int row = 0; row<24; row++)
    {
        for (int col = 0; col < 40; col++)
        {
            type = map[row][col];
            dest.x = col * 32;
            dest.y = row * 32;

            switch(type) {
            case 0:
                TextureManager::Draw(water, src, dest);
                break;
            case 1:
                TextureManager::Draw(grass, src, dest);
                break;
            case 2:
                TextureManager::Draw(dirt, src, dest);
                break;
            default:
                break;
            }
        }
    }
}
